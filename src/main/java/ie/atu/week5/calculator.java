package ie.atu.week5;

public class calculator
{
    private int value1;
    private int value2;
    private int value3;
    private int result;

    public calculator()
    {
        value1 = 0;
        value2 = 0;
        value3 = 0;
        result = 0;
    }
    public int add(int value1, int value2, int value3)
    {
        result = value1 + value2 + value3;
        return result;
    }





}
